package Calculadora;

public class Operaciones {

    int suma (int num1, int num2){
        return num1 + num2;
    }

    double sumar(double num1, double num2){ //entonces ahora son 2 metodos suma, overload.
        return num1 + num2;
    }

    int restar(int num1, int num2){
        return num1 - num2;
    }

}
